public function getLicenseQuestionContentsAnswerAndPoint(Request $request)
{

  $contents = [];
  $answers = [];

  $license_question = License_question::select(
    'license.id as license_id', #試験ＩＤ
    'license.name as license_name', #試験名
    'license_schedule.id as license_schedule_id', #試験スケジュールＩＤ
    'license_schedule.license_year', #試験年度
    'license_schedule.license_phase', #試験フェーズ（１か２）
    'license_schedule.name as schedule_name', #（一次試験、か、二次試験）
    'license_examination_subject.id as license_examination_subject_id', #試験科目ＩＤ、全科目は「9999999」
    'license_examination_subject.name as subject_name', #試験科目名、全科目は「全科目」
    'license_examination_subject.step as subject_step', #試験科目番号、全科目は「」
    'license_examination_subject.time as subject_time', #試験時間、全科目は「全科目」
    'license_question.id as license_question_id',
    'license_question.number as license_question_number',
    'license_question.commentary'
    )
    ->leftjoin('license', 'license.id', '=', 'license_question.license_id')
    ->leftjoin('license_schedule', 'license_schedule.id', '=', 'license_question.license_schedule_id')
    ->leftjoin('license_examination_subject', 'license_examination_subject.id', '=', 'license_question.license_examination_subject_id')
    ->where('license_question.license_id',1)
    ->where('license_schedule.license_year',2017)
    ->orderBy('license_examination_subject.id')
    ->orderBy('license_question.number')
    ->paginate(20);
  
  foreach($license_question as $key=>$val){

    $contents[$val->license_question_id] = License_question_contents::select('id','license_question_contents_answer_id','number','level','points')->where('license_question_id',$val->license_question_id)->get();

    foreach($contents[$val->license_question_id] as $key2=>$val2){

      $answers[$val2->id] = License_question_contents_answer::select('id')->where('license_question_contents_id',$val2->id)->get();
      
    }

  }
    
  if ($request->has('page')) {
      return json_encode(View::make('manager.include.license_check', array('license_question'=>$license_question, 'contents'=>$contents, 'answers'=>$answers))->render());
  }

  return View('manager.license.check', compact(
      'license_question',
      'contents',
      'answers'
  ));

}






public function postChangeLicenseQuestionContentsPoint(Request $request)
{

  License_question_contents::where('id',$request->get('content_id'))
    ->update([ 'points'=>$request->get('points') ]);

  return 'ok';

}






