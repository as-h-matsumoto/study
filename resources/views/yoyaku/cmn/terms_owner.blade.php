<h1>オーナー様向けルール</h1>

<p>オーナー様向けルールは、オーナー様が予約受付を行う際のルールや厳守する事項が明記されております。</p>
<p>これらの内容は、Coordiy予約を利用するすべてのオーナー様に適用されます。</p>

